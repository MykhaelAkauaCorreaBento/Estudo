//Retornando partes de string
//Quando lidamos com variáveis do tipo string, em algumas situações,
//podemos precisar retornar apenas parte da string.
//Para isso o JavaScript fornece um método conhecido como substring().


//O 'substring' é um método nativo do JavaScript que nos permite extrair partes de uma string.

let yugioh="Este lendário dragão é uma poderosa máquina de destruição. Praticamente invencível, muito poucos enfrentaram essa magnifica criatura e viveram para contar a história.";
let resultado = yugioh.substring(5, 13);
console.log(resultado);



