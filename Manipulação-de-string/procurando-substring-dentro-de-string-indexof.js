//Procurando substring dentro de string

//Procurar uma substring dentro de uma string é uma tarefa
//comum na rotina do desenvolvedor. E para isso há um recurso nativo que faz esse trabalho.

//indexOf método nativo

//Para pesquisar por uma substring dentro de uma string basta usar o método 'indexOf'.

let frase = "A Austrália é um país maravilhoso!";
let resultado = frase.indexOf("Austrália");

console.log(resultado);

//Quando o método indexOf encontra a substring dentro de uma string,
//ele retorna a posição onde a substring foi encontrada. Caso não encontre
//ele retorna o valor -1.

