//Obtendo o tamanho de strings

//Para obter o tamanho de uma string basta utilizar a propriedade length.

let cep = "23012-900";

//Obtendo o tamanho da string 'length'
//O tamanho da string representa a quantidade de caracteres que ela tem.
if(cep.length !=9){
    console.log("O CEP deve ter 9 caracteres");
}else{
    console.log("O CEP "+cep+" está correto.");
}

