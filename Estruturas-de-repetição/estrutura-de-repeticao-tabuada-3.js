//Estrutura de repetição while


let numero = 3;
let contador = 0;

console.log("tabuada de multiplicação - Nº " + numero );

while (contador <=10){
    let resultado = numero * contador;

    console.log( numero + " x " + contador + " = " + resultado);

    contador++;
}



