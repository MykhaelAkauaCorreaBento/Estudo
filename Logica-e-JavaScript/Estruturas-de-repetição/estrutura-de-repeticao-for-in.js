//A estrutura de repetição for .. in permite que todas as propriedades de 
//um objeto sejam percorridas

let aluno = {nome:"Marcos", email:"marcos@email.com", idade:25};

for(let dados in aluno){
    console.log(aluno[dados]);
}

//A estrutura de repetição for ..in percorrendo objeto.
//'dados' representa a propriedade do objeto que está sendo iterada.

//A estrutura for..in otera cada propriedade de um objeto,seguindo a ordem que elas
//estão no objeto.


